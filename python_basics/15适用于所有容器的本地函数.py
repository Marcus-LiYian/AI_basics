# sorted(数据容器，reverse=False)，要求容器内数据类型统一，对容器内的数据默认从小到大排序，返回排列完成的新容器，不会改变原容器。若都是是字符串，则按照对应位置字母Unicode编码进行比较。

# len(数据容器)，获取容器中元素的个数，返回元素个数。

# max(数据容器)，要求容器内数据类型统一，返回容器中的或多个值中的最大值。

# min(数据容器)，要求容器内数据类型统一，返回容器中的或多个值中的最小值。

# sum(数据容器)，注意，这个函数仅针对内容是纯数值的容器，字符串不能使用sum，返回所有数据的和。

# 公共函数功能就是：取大取小取总和、排序、测长度。